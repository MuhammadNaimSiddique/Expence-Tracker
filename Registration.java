    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        
        String Uname = jTextField1.getText();
        String Email = jTextField2.getText();
        String Pass1 = String.valueOf(jPasswordField1.getPassword());
        String Pass2 = String.valueOf(jPasswordField2.getPassword());
        String Phone = jTextField5.getText();
        String gender = "Male";
        
        if (jRadioButton_Female.isSelected()) {
            gender = "Female";
        }
        if (verifyFields()) {
            if (!checkUserEmail(Email)) {
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO `user`(`user_name`, `email`, `password`, `phone`, `gender`) VALUES (?,?,?,?,?)";
                
                try {
                    ps = My_CNX.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, Uname);
                    ps.setString(2, Email);
                    ps.setString(3, Pass1);
                    ps.setString(4, Phone);
                    ps.setString(5, gender);
                    
                    if (ps.executeUpdate() != 0) {
                        JOptionPane.showMessageDialog(null, "Your Account has been created");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error: Check your information");
                    }
                    new Login().setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
                    new Login().setVisible(true);
                    this.setVisible(false);
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }//GEN-LAST:event_jButton_RegisterActionPerformed

