    public boolean verifyFields() {
        String Uname = jTextField1.getText();
        String Email = jTextField2.getText();
        String Phone = jTextField5.getText();
        String Pass1 = String.valueOf(jPasswordField1.getPassword());
        String Pass2 = String.valueOf(jPasswordField2.getPassword());

        // Check Empty Feilds
        if (Uname.trim().equals("") || Email.trim().equals("") || Phone.trim().equals("")
                || Pass1.trim().equals("") || Pass2.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "One or more fields are Empty", "Empty Fields", 2);
            return false;
        } // Check two password are equal
        else if (!Pass1.equals(Pass2)) {
            JOptionPane.showMessageDialog(null, "Password Doesn't Match", "Confirm Password", 2);
            return false;
        } // If everything is ok
        else {
            return true;
        }
    }

    // create a unction to check if the the entered email already exits in the database
    public boolean checkUserEmail(String email) {
        PreparedStatement st;
        ResultSet rs;
        boolean email_exist = false;
        
        String query = "SELECT * FROM `user` WHERE `email` = ?";
        
        try {
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, email);
            rs = st.executeQuery();
            
            if (rs.next()) {
                email_exist = true;
                JOptionPane.showMessageDialog(null, "This email is already taken", "Choose Another one", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        return email_exist;
    }

